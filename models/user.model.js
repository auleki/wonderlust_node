import {model, Schema} from 'mongoose'

const userModel = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    // settings: {
        
    // }

}, {timestamps: true})

export default model('User', userModel)